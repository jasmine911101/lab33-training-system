-- Commerce Sprint 2: Team Training Product Delivery
-- Adds team delivery without payment providers. Do not run directly in production
-- until reviewed and backed up.

create table if not exists public.teams (
  id bigint generated by default as identity primary key,
  name text not null,
  description text,
  sport_type text,
  created_by bigint not null references public.coaches(id) on delete restrict,
  is_active boolean not null default true,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now()
);

create table if not exists public.team_memberships (
  id bigint generated by default as identity primary key,
  team_id bigint not null references public.teams(id) on delete restrict,
  athlete_id bigint not null references public.athletes(id) on delete restrict,
  status text not null default 'active' check (status in ('active', 'inactive', 'removed')),
  joined_at timestamp with time zone not null default now(),
  left_at timestamp with time zone,
  created_at timestamp with time zone not null default now()
);

create unique index if not exists idx_team_memberships_one_active_member
  on public.team_memberships (team_id, athlete_id)
  where status = 'active';

create table if not exists public.team_product_enrollments (
  id bigint generated by default as identity primary key,
  team_id bigint not null references public.teams(id) on delete restrict,
  product_id bigint not null references public.training_products(id) on delete restrict,
  product_version_id bigint not null references public.training_product_versions(id) on delete restrict,
  assigned_by bigint not null references public.coaches(id) on delete restrict,
  start_date date not null,
  end_date date,
  seat_limit integer check (seat_limit is null or seat_limit > 0),
  status text not null default 'active' check (status in ('draft', 'active', 'ended', 'cancelled')),
  access_mode text not null default 'dynamic_roster' check (access_mode in ('dynamic_roster')),
  created_at timestamp with time zone not null default now(),
  activated_at timestamp with time zone,
  ended_at timestamp with time zone,
  cancelled_at timestamp with time zone,
  constraint team_product_enrollments_date_check check (end_date is null or end_date >= start_date)
);

create unique index if not exists idx_team_product_enrollments_one_active_version
  on public.team_product_enrollments (team_id, product_version_id)
  where status = 'active';

create table if not exists public.team_program_instances (
  id bigint generated by default as identity primary key,
  team_product_enrollment_id bigint not null references public.team_product_enrollments(id) on delete restrict,
  team_id bigint not null references public.teams(id) on delete restrict,
  product_version_id bigint not null references public.training_product_versions(id) on delete restrict,
  name text not null,
  start_date date not null,
  end_date date,
  timezone text not null default 'Asia/Taipei',
  status text not null default 'active' check (status in ('scheduled', 'active', 'completed', 'cancelled')),
  source_snapshot_json jsonb,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint team_program_instances_one_main_program unique (team_product_enrollment_id),
  constraint team_program_instances_date_check check (end_date is null or end_date >= start_date)
);

create table if not exists public.team_program_sessions (
  id bigint generated by default as identity primary key,
  team_program_instance_id bigint not null references public.team_program_instances(id) on delete restrict,
  source_product_block_id bigint not null references public.training_product_blocks(id) on delete restrict,
  source_block_id bigint not null references public.blocks(id) on delete restrict,
  title text not null,
  scheduled_date date,
  week_number integer check (week_number is null or week_number > 0),
  day_number integer check (day_number is null or day_number > 0),
  sort_order integer not null default 0 check (sort_order >= 0),
  status text not null default 'scheduled' check (status in ('scheduled', 'available', 'completed', 'cancelled')),
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint team_program_sessions_program_sort_key unique (team_program_instance_id, sort_order)
);

create table if not exists public.athlete_team_session_results (
  id bigint generated by default as identity primary key,
  team_program_session_id bigint not null references public.team_program_sessions(id) on delete restrict,
  athlete_id bigint not null references public.athletes(id) on delete restrict,
  status text not null default 'not_started' check (status in ('not_started', 'in_progress', 'completed', 'skipped')),
  started_at timestamp with time zone,
  completed_at timestamp with time zone,
  notes text,
  result_json jsonb,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint athlete_team_session_results_session_athlete_key unique (team_program_session_id, athlete_id)
);

create index if not exists idx_teams_created_by on public.teams (created_by);
create index if not exists idx_team_memberships_team_status on public.team_memberships (team_id, status);
create index if not exists idx_team_memberships_athlete_status on public.team_memberships (athlete_id, status);
create index if not exists idx_team_product_enrollments_team_status on public.team_product_enrollments (team_id, status);
create index if not exists idx_team_product_enrollments_version on public.team_product_enrollments (product_version_id);
create index if not exists idx_team_program_instances_team_status on public.team_program_instances (team_id, status);
create index if not exists idx_team_program_instances_enrollment on public.team_program_instances (team_product_enrollment_id);
create index if not exists idx_team_program_sessions_program_order on public.team_program_sessions (team_program_instance_id, sort_order);
create index if not exists idx_athlete_team_session_results_athlete on public.athlete_team_session_results (athlete_id);

