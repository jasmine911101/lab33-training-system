-- Commerce Sprint 1.1: Product Management Hardening
-- Introduces product versions, transactional block replacement, lifecycle RPCs,
-- and backfills existing Sprint 1 product/block data without deleting products.

alter table public.training_products
  add column if not exists archived_at timestamp with time zone;

create table if not exists public.training_product_versions (
  id bigint generated by default as identity primary key,
  product_id bigint not null references public.training_products(id) on delete restrict,
  version_number integer not null check (version_number > 0),
  status text not null default 'draft' check (status in ('draft', 'published', 'retired')),
  snapshot_name text not null,
  snapshot_description text,
  snapshot_price_amount integer not null default 0 check (snapshot_price_amount >= 0),
  snapshot_currency text not null default 'TWD' check (snapshot_currency in ('TWD', 'USD')),
  created_at timestamp with time zone not null default now(),
  published_at timestamp with time zone,
  retired_at timestamp with time zone,
  constraint training_product_versions_product_version_key unique (product_id, version_number)
);

create unique index if not exists idx_training_product_versions_one_published
  on public.training_product_versions (product_id)
  where status = 'published';

create index if not exists idx_training_product_versions_product_id
  on public.training_product_versions (product_id);

create index if not exists idx_training_product_versions_status
  on public.training_product_versions (status);

alter table public.training_product_versions enable row level security;

grant select, insert, update, delete on public.training_product_versions to authenticated;
grant usage, select on sequence public.training_product_versions_id_seq to authenticated;

-- Backfill one version per existing product if Sprint 1 data exists.
insert into public.training_product_versions (
  product_id,
  version_number,
  status,
  snapshot_name,
  snapshot_description,
  snapshot_price_amount,
  snapshot_currency,
  created_at,
  published_at,
  retired_at
)
select
  p.id,
  1,
  case
    when p.status = 'published' then 'published'
    when p.status = 'archived' then 'retired'
    else 'draft'
  end,
  p.name,
  p.description,
  coalesce(p.price_amount, 0),
  case when p.currency in ('TWD', 'USD') then p.currency else 'TWD' end,
  coalesce(p.created_at, now()),
  case when p.status = 'published' then coalesce(p.published_at, now()) else null end,
  case when p.status = 'archived' then coalesce(p.unpublished_at, now()) else null end
from public.training_products p
where not exists (
  select 1
  from public.training_product_versions v
  where v.product_id = p.id
);

-- Upgrade Sprint 1 mapping table from product_id to product_version_id.
alter table public.training_product_blocks
  add column if not exists product_version_id bigint references public.training_product_versions(id) on delete cascade,
  add column if not exists week_number integer check (week_number is null or week_number > 0),
  add column if not exists day_number integer check (day_number is null or day_number > 0);

do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'training_product_blocks'
      and column_name = 'product_id'
  ) then
    update public.training_product_blocks tpb
    set product_version_id = v.id
    from public.training_product_versions v
    where tpb.product_version_id is null
      and tpb.product_id = v.product_id
      and v.version_number = 1;
  end if;
end $$;

