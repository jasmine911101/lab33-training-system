create or replace function public.replace_product_version_blocks(
  p_product_version_id bigint,
  p_blocks jsonb,
  p_actor_coach_id bigint,
  p_actor_is_head_coach boolean
)
returns void
language plpgsql
as $$
declare
  version_row public.training_product_versions%rowtype;
  product_author_id bigint;
  block_item jsonb;
  next_block_id bigint;
  next_week integer;
  next_day integer;
  next_sort integer;
  seen_sorts integer[] := array[]::integer[];
begin
  select *
    into version_row
  from public.training_product_versions
  where id = p_product_version_id
  for update;

  if version_row.id is null then
    raise exception '找不到商品版本。';
  end if;

  select author_coach_id
    into product_author_id
  from public.training_products
  where id = version_row.product_id
  for update;

  if not (coalesce(p_actor_is_head_coach, false) or product_author_id = p_actor_coach_id) then
    raise exception '你沒有權限修改這個商品版本。';
  end if;

  if version_row.status <> 'draft' then
    raise exception 'Published version is read-only. 請先建立新的 draft version。';
  end if;

  if jsonb_typeof(coalesce(p_blocks, '[]'::jsonb)) <> 'array' then
    raise exception 'Blocks payload 格式不正確。';
  end if;

  delete from public.training_product_blocks
  where product_version_id = p_product_version_id;

  for block_item in select * from jsonb_array_elements(coalesce(p_blocks, '[]'::jsonb)) loop
    next_block_id := nullif(block_item->>'block_id', '')::bigint;
    next_week := nullif(block_item->>'week_number', '')::integer;
    next_day := nullif(block_item->>'day_number', '')::integer;
    next_sort := nullif(block_item->>'sort_order', '')::integer;

    if next_block_id is null or next_sort is null then
      raise exception 'Block payload 缺少 block_id 或 sort_order。';
    end if;

    if next_sort < 0 then
      raise exception 'sort_order 必須大於或等於 0。';
    end if;

    if next_week is not null and next_week <= 0 then
      raise exception 'week_number 必須為正整數。';
    end if;

    if next_day is not null and next_day <= 0 then
      raise exception 'day_number 必須為正整數。';
    end if;

    if next_sort = any(seen_sorts) then
      raise exception 'sort_order 不可重複。';
    end if;
    seen_sorts := array_append(seen_sorts, next_sort);

    if not exists (select 1 from public.blocks where id = next_block_id) then
      raise exception '商品包含不存在的板塊。';
    end if;

    insert into public.training_product_blocks (
      product_version_id,
      block_id,
      week_number,
      day_number,
      sort_order
    ) values (
      p_product_version_id,
      next_block_id,
      next_week,
      next_day,
      next_sort
    );
  end loop;
end;
$$;

create or replace function public.publish_product_version(
  p_product_id bigint,
  p_product_version_id bigint,
  p_actor_coach_id bigint,
  p_actor_is_head_coach boolean
)
returns void
language plpgsql
as $$
declare
  version_row public.training_product_versions%rowtype;
  product_row public.training_products%rowtype;
  block_count integer;
  published_time timestamp with time zone := now();
begin
  select * into product_row
  from public.training_products
  where id = p_product_id
  for update;

  if product_row.id is null then
    raise exception '找不到商品。';
  end if;

  if not (coalesce(p_actor_is_head_coach, false) or product_row.author_coach_id = p_actor_coach_id) then
    raise exception '你沒有權限發布這個商品。';
  end if;

  select * into version_row
  from public.training_product_versions
  where id = p_product_version_id
    and product_id = p_product_id
  for update;

  if version_row.id is null then
    raise exception '找不到商品版本。';
  end if;

  if version_row.status <> 'draft' then
    raise exception '只能發布 draft version。';
  end if;

  if btrim(version_row.snapshot_name) = '' then
    raise exception '商品名稱不可為空。';
  end if;

  if version_row.snapshot_price_amount < 0 then
    raise exception '商品價格不可小於 0。';
  end if;

  if version_row.snapshot_currency not in ('TWD', 'USD') then
    raise exception '商品幣別不支援。';
  end if;

  select count(*) into block_count
  from public.training_product_blocks
  where product_version_id = p_product_version_id;

  if block_count < 1 then
    raise exception '商品至少需要包含一個 Block 才能發布。';
  end if;

  update public.training_product_versions
  set status = 'retired', retired_at = published_time
  where product_id = p_product_id
    and status = 'published'
    and id <> p_product_version_id;

  update public.training_product_versions
  set status = 'published', published_at = published_time, retired_at = null
  where id = p_product_version_id;

  update public.training_products
  set name = version_row.snapshot_name,
      description = version_row.snapshot_description,
      price_amount = version_row.snapshot_price_amount,
      currency = version_row.snapshot_currency,
      status = 'published',
      is_active = true,
      published_at = published_time,
      unpublished_at = null,
      archived_at = null,
      updated_at = published_time
  where id = p_product_id;
end;
$$;

create or replace function public.unpublish_product(
  p_product_id bigint,
  p_actor_coach_id bigint,
  p_actor_is_head_coach boolean
)
returns void
language plpgsql
as $$
declare
  product_row public.training_products%rowtype;
  unpublished_time timestamp with time zone := now();
begin
  select * into product_row
  from public.training_products
  where id = p_product_id
  for update;

  if product_row.id is null then
    raise exception '找不到商品。';
  end if;

  if not (coalesce(p_actor_is_head_coach, false) or product_row.author_coach_id = p_actor_coach_id) then
    raise exception '你沒有權限下架這個商品。';
  end if;

  update public.training_products
  set status = 'draft',
      is_active = false,
      unpublished_at = unpublished_time,
      updated_at = unpublished_time
  where id = p_product_id;
end;
$$;

create or replace function public.archive_product(
  p_product_id bigint,
  p_actor_coach_id bigint,
  p_actor_is_head_coach boolean
)
returns void
language plpgsql
as $$
declare
  product_row public.training_products%rowtype;
  archived_time timestamp with time zone := now();
begin
  select * into product_row
  from public.training_products
  where id = p_product_id
  for update;

  if product_row.id is null then
    raise exception '找不到商品。';
  end if;

  if not (coalesce(p_actor_is_head_coach, false) or product_row.author_coach_id = p_actor_coach_id) then
    raise exception '你沒有權限封存這個商品。';
  end if;

  update public.training_product_versions
  set status = 'retired', retired_at = coalesce(retired_at, archived_time)
  where product_id = p_product_id
    and status = 'draft';

  update public.training_products
  set status = 'archived',
      is_active = false,
      unpublished_at = archived_time,
      archived_at = archived_time,
      updated_at = archived_time
  where id = p_product_id;
end;
$$;

