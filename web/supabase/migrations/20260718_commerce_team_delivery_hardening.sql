create or replace function public.current_coach_id()
returns bigint
language sql
stable
as $$
  select c.id
  from public.coaches c
  where c.user_id = (select auth.uid())
  limit 1;
$$;

create or replace function public.current_coach_is_head()
returns boolean
language sql
stable
as $$
  select coalesce((
    select c.is_head_coach
    from public.coaches c
    where c.user_id = (select auth.uid())
    limit 1
  ), false);
$$;

create or replace function public.coach_has_team_permission(p_team_id bigint, p_permission text)
returns boolean
language sql
stable
as $$
  select public.current_coach_is_head()
    or exists (
      select 1
      from public.team_coaches tc
      where tc.team_id = p_team_id
        and tc.coach_id = public.current_coach_id()
        and tc.status = 'active'
        and (
          p_permission = 'access'
          or tc.role = 'owner'
          or (p_permission = 'roster' and tc.can_manage_roster)
          or (p_permission = 'programs' and tc.can_manage_programs)
          or (p_permission = 'results' and tc.can_view_results)
          or (p_permission = 'coaches' and tc.role = 'owner')
        )
    );
$$;

create or replace function public.coach_can_manage_team(p_team_id bigint)
returns boolean
language sql
stable
as $$
  select public.coach_has_team_permission(p_team_id, 'roster')
    or public.coach_has_team_permission(p_team_id, 'programs')
    or public.coach_has_team_permission(p_team_id, 'coaches');
$$;

create or replace function public.coach_can_manage_team_roster(p_team_id bigint)
returns boolean language sql stable as $$ select public.coach_has_team_permission(p_team_id, 'roster'); $$;

create or replace function public.coach_can_manage_team_programs(p_team_id bigint)
