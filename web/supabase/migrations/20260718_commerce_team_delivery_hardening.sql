create or replace function public.add_or_reactivate_team_member(
  p_team_id bigint,
  p_athlete_id bigint,
  p_actor_coach_id bigint,
  p_actor_is_head_coach boolean
)
returns table (membership_id bigint, active_roster_count integer, effective_seat_limit integer)
language plpgsql
as $$
declare
  active_count integer;
  strict_limit integer;
  inactive_membership_id bigint;
  new_membership_id bigint;
begin
  if p_actor_coach_id is null then
    raise exception '缺少操作者。';
  end if;

  perform 1 from public.teams where id = p_team_id and is_active = true for update;
  if not found then
    raise exception '找不到球隊。';
  end if;

  if not (
    coalesce(p_actor_is_head_coach, false)
    or exists (
      select 1 from public.team_coaches tc
      where tc.team_id = p_team_id
        and tc.coach_id = p_actor_coach_id
        and tc.status = 'active'
        and (tc.role = 'owner' or tc.can_manage_roster)
    )
  ) then
    raise exception '你沒有權限管理這支球隊 roster。';
  end if;

  perform 1 from public.athletes where id = p_athlete_id;
  if not found then
    raise exception '找不到這位學員。';
  end if;

  perform 1
  from public.team_product_enrollments e
  where e.team_id = p_team_id
    and e.status = 'active'
    and e.start_date <= current_date
    and (e.end_date is null or e.end_date >= current_date)
  for update;

  select min(e.seat_limit) into strict_limit
  from public.team_product_enrollments e
  where e.team_id = p_team_id
    and e.status = 'active'
    and e.start_date <= current_date
    and (e.end_date is null or e.end_date >= current_date)
    and e.seat_limit is not null;

  if exists (
    select 1 from public.team_memberships tm
    where tm.team_id = p_team_id
      and tm.athlete_id = p_athlete_id
      and tm.status = 'active'
  ) then
    raise exception '這位學員已經是 active team member。';
  end if;

  select count(*) into active_count
  from public.team_memberships tm
  where tm.team_id = p_team_id
    and tm.status = 'active';

  if strict_limit is not null and active_count + 1 > strict_limit then
    raise exception '此球隊目前有效課程的最嚴格席次上限為 % 人，已達上限。', strict_limit;
  end if;

  select tm.id into inactive_membership_id
  from public.team_memberships tm
  where tm.team_id = p_team_id
    and tm.athlete_id = p_athlete_id
    and tm.status = 'inactive'
  order by tm.id desc
  limit 1
  for update;

  if inactive_membership_id is not null then
    update public.team_memberships
    set status = 'active', left_at = null, joined_at = now()
    where id = inactive_membership_id
    returning id into new_membership_id;
  else
    insert into public.team_memberships (team_id, athlete_id, status)
    values (p_team_id, p_athlete_id, 'active')
    returning id into new_membership_id;
  end if;

  return query select new_membership_id, active_count + 1, strict_limit;
end;
$$;

revoke execute on function public.add_or_reactivate_team_member(bigint, bigint, bigint, boolean) from public;
revoke execute on function public.add_or_reactivate_team_member(bigint, bigint, bigint, boolean) from anon;
revoke execute on function public.add_or_reactivate_team_member(bigint, bigint, bigint, boolean) from authenticated;
grant execute on function public.add_or_reactivate_team_member(bigint, bigint, bigint, boolean) to service_role;

create or replace function public.transition_team_enrollment(
  p_enrollment_id bigint,
  p_action text,
  p_actor_coach_id bigint,
  p_actor_is_head_coach boolean
)
returns table (enrollment_id bigint, program_instance_id bigint, enrollment_status text, program_status text)
language plpgsql
as $$
declare
  enrollment_record public.team_product_enrollments%rowtype;
  program_record public.team_program_instances%rowtype;
  next_enrollment_status text;
  next_program_status text;
begin
  if p_actor_coach_id is null then
    raise exception '缺少操作者。';
  end if;
  if p_action not in ('end', 'cancel') then
    raise exception '不支援的狀態轉換。';
  end if;

  select * into enrollment_record
  from public.team_product_enrollments
  where id = p_enrollment_id
  for update;
  if not found then
    raise exception '找不到 enrollment。';
  end if;

  if not (
    coalesce(p_actor_is_head_coach, false)
    or exists (
      select 1 from public.team_coaches tc
      where tc.team_id = enrollment_record.team_id
        and tc.coach_id = p_actor_coach_id
        and tc.status = 'active'
        and (tc.role = 'owner' or tc.can_manage_programs)
    )
  ) then
    raise exception '你沒有權限管理這支球隊課表。';
  end if;

  if enrollment_record.status in ('ended', 'cancelled') then
    raise exception '此 enrollment 已經結束或取消。';
  end if;

  select * into program_record
  from public.team_program_instances
  where team_product_enrollment_id = p_enrollment_id
  for update;

  next_enrollment_status := case when p_action = 'end' then 'ended' else 'cancelled' end;
  next_program_status := case when p_action = 'end' then 'completed' else 'cancelled' end;

  update public.team_product_enrollments
  set status = next_enrollment_status,
      ended_at = case when p_action = 'end' then now() else ended_at end,
      cancelled_at = case when p_action = 'cancel' then now() else cancelled_at end
  where id = p_enrollment_id;

  if found and program_record.id is not null then
    update public.team_program_instances
    set status = next_program_status,
        updated_at = now()
    where id = program_record.id;

    if p_action = 'cancel' then
      update public.team_program_sessions
      set status = 'cancelled', updated_at = now()
      where team_program_instance_id = program_record.id
        and status <> 'completed';
    end if;
  end if;

  return query select p_enrollment_id, program_record.id, next_enrollment_status, next_program_status;
end;
$$;

revoke execute on function public.transition_team_enrollment(bigint, text, bigint, boolean) from public;
revoke execute on function public.transition_team_enrollment(bigint, text, bigint, boolean) from anon;
revoke execute on function public.transition_team_enrollment(bigint, text, bigint, boolean) from authenticated;
grant execute on function public.transition_team_enrollment(bigint, text, bigint, boolean) to service_role;

-- Replace Sprint 2 assignment RPC with team_coaches permission and row locks.
create or replace function public.assign_product_version_to_team(
  p_team_id bigint,
  p_product_version_id bigint,
  p_actor_coach_id bigint,
  p_actor_is_head_coach boolean,
  p_start_date date,
  p_end_date date,
  p_seat_limit integer,
  p_timezone text
)
returns table (
  enrollment_id bigint,
  program_instance_id bigint,
  sessions_count integer
)
language plpgsql
as $$
declare
  team_record public.teams%rowtype;
  version_record public.training_product_versions%rowtype;
  product_record public.training_products%rowtype;
  block_count integer;
  active_roster_count integer;
  next_enrollment_id bigint;
  next_program_id bigint;
  clean_timezone text := coalesce(nullif(btrim(p_timezone), ''), 'Asia/Taipei');
begin
  if p_actor_coach_id is null then
    raise exception '缺少操作者。';
  end if;

  select * into team_record from public.teams where id = p_team_id and is_active = true for update;
  if not found then
    raise exception '找不到球隊。';
  end if;

  if not (
    coalesce(p_actor_is_head_coach, false)
    or exists (
      select 1 from public.team_coaches tc
      where tc.team_id = p_team_id
        and tc.coach_id = p_actor_coach_id
        and tc.status = 'active'
        and (tc.role = 'owner' or tc.can_manage_programs)
    )
  ) then
    raise exception '你沒有權限指派商品給這支球隊。';
  end if;

  if p_start_date is null then
    raise exception '請選擇開始日期。';
  end if;

  if p_end_date is not null and p_end_date < p_start_date then
    raise exception '結束日期不可早於開始日期。';
  end if;

  if p_seat_limit is not null and p_seat_limit <= 0 then
    raise exception '席次上限必須大於 0。';
  end if;

  if clean_timezone !~ '^[A-Za-z_]+/[A-Za-z0-9_+\-]+(/[A-Za-z0-9_+\-]+)?$' then
    raise exception 'Timezone 格式不正確。';
  end if;

  select * into version_record from public.training_product_versions where id = p_product_version_id;
  if not found or version_record.status <> 'published' then
    raise exception '只能指派 Published Product Version。';
  end if;

  select * into product_record from public.training_products where id = version_record.product_id;
  if not found or product_record.is_active is not true or product_record.status = 'archived' then
    raise exception '商品目前不可指派。';
  end if;

  if not (coalesce(p_actor_is_head_coach, false) or product_record.author_coach_id = p_actor_coach_id) then
    raise exception '你沒有權限指派這個商品。';
  end if;

  select count(*) into block_count
  from public.training_product_blocks
  where product_version_id = p_product_version_id;

  if block_count <= 0 then
    raise exception 'Published version 至少需要一個 Block。';
  end if;

  perform 1
  from public.team_product_enrollments e
  where e.team_id = p_team_id
  for update;

  if exists (
    select 1
    from public.team_product_enrollments e
    where e.team_id = p_team_id
      and e.product_version_id = p_product_version_id
      and e.status = 'active'
  ) then
    raise exception '這支球隊已經有相同 Product Version 的 active enrollment。';
  end if;

  select count(*) into active_roster_count
  from public.team_memberships
  where team_id = p_team_id and status = 'active';

  if p_seat_limit is not null and active_roster_count > p_seat_limit then
    raise exception '目前 active roster 人數已超過席次上限。';
  end if;

  insert into public.team_product_enrollments (
    team_id,
    product_id,
    product_version_id,
    assigned_by,
    start_date,
    end_date,
    seat_limit,
    status,
    access_mode,
    activated_at
  ) values (
    p_team_id,
    product_record.id,
    version_record.id,
    p_actor_coach_id,
    p_start_date,
    p_end_date,
    p_seat_limit,
    'active',
    'dynamic_roster',
    case when p_start_date <= current_date then now() else null end
  ) returning id into next_enrollment_id;

  insert into public.team_program_instances (
    team_product_enrollment_id,
    team_id,
    product_version_id,
    name,
    start_date,
    end_date,
    timezone,
    status,
    source_snapshot_json
  ) values (
    next_enrollment_id,
    p_team_id,
    version_record.id,
    version_record.snapshot_name,
    p_start_date,
    p_end_date,
    clean_timezone,
    case when p_start_date > current_date then 'scheduled' else 'active' end,
    jsonb_build_object(
      'product_id', product_record.id,
      'product_name', product_record.name,
      'product_version_id', version_record.id,
      'version_number', version_record.version_number,
      'snapshot_name', version_record.snapshot_name,
      'generated_at', now()
    )
  ) returning id into next_program_id;

  insert into public.team_program_sessions (
    team_program_instance_id,
    source_product_block_id,
    source_block_id,
    title,
    scheduled_date,
    week_number,
    day_number,
    sort_order,
    status
  )
  select
    next_program_id,
    tpb.id,
    tpb.block_id,
    coalesce(nullif(b.block_name, ''), nullif(b.block_code, ''), version_record.snapshot_name),
    case
      when tpb.week_number is not null and tpb.day_number is not null then p_start_date + (((tpb.week_number - 1) * 7 + (tpb.day_number - 1))::integer)
      else null
    end,
    tpb.week_number,
    tpb.day_number,
    tpb.sort_order,
    'scheduled'
  from public.training_product_blocks tpb
  join public.blocks b on b.id = tpb.block_id
  where tpb.product_version_id = version_record.id
  order by tpb.sort_order;

  return query select next_enrollment_id, next_program_id, block_count;
end;
