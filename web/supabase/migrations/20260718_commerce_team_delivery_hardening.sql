create table if not exists public.team_coaches (
  id bigint generated by default as identity primary key,
  team_id bigint not null references public.teams(id) on delete restrict,
  coach_id bigint not null references public.coaches(id) on delete restrict,
  role text not null default 'coach' check (role in ('owner', 'manager', 'coach', 'viewer')),
  can_manage_roster boolean not null default false,
  can_manage_programs boolean not null default false,
  can_view_results boolean not null default true,
  status text not null default 'active' check (status in ('active', 'inactive', 'removed')),
  assigned_by bigint references public.coaches(id) on delete set null,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  removed_at timestamp with time zone
);

create unique index if not exists idx_team_coaches_one_active_coach
  on public.team_coaches (team_id, coach_id)
  where status = 'active';

create index if not exists idx_team_coaches_team_status on public.team_coaches (team_id, status);
create index if not exists idx_team_coaches_coach_status on public.team_coaches (coach_id, status);
create index if not exists idx_team_coaches_owner_guard on public.team_coaches (team_id, role, status);

grant select, insert, update, delete on public.team_coaches to authenticated;
grant usage, select on sequence public.team_coaches_id_seq to authenticated;
alter table public.team_coaches enable row level security;

insert into public.team_coaches (
  team_id,
  coach_id,
  role,
  can_manage_roster,
  can_manage_programs,
  can_view_results,
  status,
  assigned_by
)
select
  t.id,
  t.created_by,
  'owner',
  true,
  true,
  true,
  'active',
  t.created_by
from public.teams t
where t.created_by is not null
  and not exists (
    select 1
    from public.team_coaches tc
    where tc.team_id = t.id
      and tc.coach_id = t.created_by
      and tc.status = 'active'
  );

create or replace function public.ensure_team_has_active_owner()
returns trigger
language plpgsql
as $$
declare
  remaining_owners integer;
begin
  if tg_op = 'DELETE' then
    if old.status = 'active' and old.role = 'owner' then
      select count(*) into remaining_owners
      from public.team_coaches
      where team_id = old.team_id
        and status = 'active'
        and role = 'owner'
        and id <> old.id;
      if remaining_owners < 1 then
        raise exception '每支 Team 至少需要一名 active owner。';
      end if;
    end if;
    return old;
  end if;

  if old.status = 'active'
     and old.role = 'owner'
     and (new.status <> 'active' or new.role <> 'owner') then
    select count(*) into remaining_owners
    from public.team_coaches
    where team_id = old.team_id
      and status = 'active'
      and role = 'owner'
      and id <> old.id;
    if remaining_owners < 1 then
      raise exception '每支 Team 至少需要一名 active owner。';
    end if;
  end if;

  new.updated_at := now();
  if new.status = 'removed' and old.status <> 'removed' then
    new.removed_at := coalesce(new.removed_at, now());
  end if;
  if new.status <> 'removed' then
    new.removed_at := null;
  end if;
  return new;
end;
$$;

drop trigger if exists trg_team_coaches_owner_guard on public.team_coaches;
create trigger trg_team_coaches_owner_guard
before update or delete on public.team_coaches
for each row execute function public.ensure_team_has_active_owner();

